    private void jbAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAdicionarActionPerformed
        // TODO add your handling code here:
        if(jtfQuantidade.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "Campo quantidade não informado!", "ERRO", JOptionPane.ERROR_MESSAGE);
            
        } else {
            
            modelProduto = controllerProduto.retornarProdutoController(Integer.parseInt(jtfCodigoProd.getText()));
            
            //ADICIONA UMA LINHA NA TABELA
            DefaultTableModel modelo = (DefaultTableModel) jTableProdutoVendas.getModel();
            int cont = 0;
            double qtd = 0;
            qtd = Double.parseDouble(jtfQuantidade.getText());
            
            for (int i = 0; i < cont; i++) {
                modelo.setNumRows(0);
            }
            
            modelo.addRow(new Object[]{
            
                modelProduto.getIdProduto(),
                modelProduto.getProNome(),
                jtfQuantidade.getText(),
                modelProduto.getProValor(),
                qtd * modelProduto.getProValor()                
                
            });
        }
        
        somarValorTotalProdutos();
        
    }//GEN-LAST:event_jbAdicionarActionPerformed

