    /**
     * Preencher o combobox com todos os clientes
     */
    private void listarClientes(){
        
        listaModelCliente = controllerCliente.listarClienteController();
        jcbNomeCliente.removeAllItems();
        
        for (int i = 0; i < listaModelCliente.size(); i++) {
            
            jcbNomeCliente.addItem(listaModelCliente.get(i).getCliNome());
            
        }
        
    }
    
    /**
     * Preencher combobox com todos os produtos
     */
    private void listarProdutos(){
        
        listaModelProduto = controllerProduto.retornarListaProdutoController();
        jcbNomeProduto.removeAllItems();
        
        for (int i = 0; i < listaModelProduto.size(); i++) {
            
            jcbNomeProduto.addItem(listaModelProduto.get(i).getProNome());
            
        }
        
    }
    
    private void carregarVendas(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTableClienteVenda.getModel();
        listaModelVendasCliente = controllerVendasCliente.retornarModelVendasCliente();
        
        int cont = listaModelVendasCliente.size();
        
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
            
                listaModelVendasCliente.get(i).getModelVendas().getIdVenda(),
                listaModelVendasCliente.get(i).getModelClientes().getCliNome(),
                listaModelVendasCliente.get(i).getModelVendas().getVendaDataVenda()
            
            });
        }
            
    }
    
    /**
     * APLICA DESCONTO AO VALOR FINAL DE VENDAS
     */
    private void aplicarDescontos(){
        
        jtfValorTotal.setText(String.valueOf(Double.parseDouble(jtfValorTotal.getText()) - Double.parseDouble(jtfDesconto.getText())));
        
    }
        
    private void somarValorTotalProdutos(){
            
            double soma=0, valor;
            int cont = jTableProdutoVendas.getRowCount();
            
            for (int i = 0; i < cont; i++) {
            
                valor = (double) jTableProdutoVendas.getValueAt(i, 4);
                soma += valor;
        }
        
        this.jtfValorTotal.setText(String.valueOf(soma));
        
        try {
            aplicarDescontos();
        } catch (Exception e) {
        }
        
    }
    
