    /**
     * SALVAR PRODUTOS NO BANCO
     * @param evt 
     */
    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:
        
        //VARIAVEIS
        int codigoVenda = 0, qtd = 0, codigoProduto = 0;
        listaModelVendasProdutos = new ArrayList<>();
        
        modelVendas.setCliente(Integer.parseInt(jtfCodigoCli.getText()));
                
        try {
            modelVendas.setVendaDataVenda(converterData.converterDataParaDateUs(new Date(System.currentTimeMillis())));
        } catch (Exception ex) {
        }        
        
        //VALOR LIQUIDO
        modelVendas.setVendaValorLiquido(Double.parseDouble(jtfValorTotal.getText()));
        //VALOR BRUTO
        modelVendas.setVendaValorBruto(Double.parseDouble(jtfValorTotal.getText()) + Double.parseDouble(jtfDesconto.getText()));
        modelVendas.setVendaDesconto(Double.parseDouble(jtfDesconto.getText()));
        
        codigoVenda = controllerVendas.salvarVendasController(modelVendas);
        
        if(codigoVenda > 0){
            JOptionPane.showMessageDialog(this, "Venda salva com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar venda!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
        int cont = jTableProdutoVendas.getRowCount();
        
        for (int i = 0; i < cont; i++) {
            //ZERANDO OS MODELs
            modelVendasProdutos = new ModelVendasProdutos();
            modelProduto = new ModelProdutos();
            
            
            codigoProduto = (int) jTableProdutoVendas.getValueAt(i, 0);
            
            modelVendasProdutos.setProduto((int) jTableProdutoVendas.getValueAt(i, 0));
            modelVendasProdutos.setVendas(codigoVenda);
            modelVendasProdutos.setVenProQuantidade(Integer.parseInt(jTableProdutoVendas.getValueAt(i, 2).toString()));
            modelVendasProdutos.setVenProValor((double) jTableProdutoVendas.getValueAt(i, 3));
            
            //PRODUTO
            modelProduto.setIdProduto((int) jTableProdutoVendas.getValueAt(i, 0));
            modelProduto.setProEstoque(controllerProduto.retornarProdutoController(codigoProduto).getProEstoque()
            - Integer.parseInt(jTableProdutoVendas.getValueAt(i, 2).toString()));
            
            listaModelVendasProdutos.add(modelVendasProdutos);
            listaModelProduto.add(modelProduto);
            
        }
        //SALVAR OS PRODUTOS DA VENDA
        if(controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)){
            //Atualizando o estoque
            controllerProduto.alterarEstoqueProdutosController(listaModelProduto);
            JOptionPane.showMessageDialog(this, "Produto da venda salvo com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            this.carregarVendas();
            this.limparFormulario();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar produtos!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

