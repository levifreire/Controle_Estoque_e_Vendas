    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "Alterar";
        habilitarDesabilitar(true);
        
        int linha = jTableUsuario.getSelectedRow();
        
        try {
            
            int codigoCliente = (int) jTableUsuario.getValueAt(linha, 0);
            
            modelUsuario = controllerUsuario.getUsuarioController(codigoCliente);
            
            this.jtfCodigo.setText(String.valueOf(modelUsuario.getIdUsuario()));
            this.jtfLogin.setText(modelUsuario.getUsuLogin());
            this.jtfNome.setText(modelUsuario.getUsuNome());
            this.jtfSenha.setText(String.valueOf(modelUsuario.getUsuSenha()));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nenhum registro foi selecionado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jbAlterarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        int linha = jTableUsuario.getSelectedRow();
        int codigoUsuario = (int) jTableUsuario.getValueAt(linha, 0);
        
        if(controllerUsuario.excluirUsuarioController(codigoUsuario)){
            JOptionPane.showMessageDialog(this, "Usuário excluido com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            carregarUsuarios();
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

