    /**
     * Salvar Usuario no banco
     */
    private void salvarUsuarios(){
        
        modelUsuario.setUsuNome(this.jtfNome.getText());
        modelUsuario.setUsuLogin(this.jtfLogin.getText());
        modelUsuario.setUsuSenha(this.jtfSenha.getText());
        
            if(controllerUsuario.salvarUsuarioController(modelUsuario)>0 && modelUsuario.getUsuSenha().equalsIgnoreCase(this.jtfConfirmarSenha.getText())){
                        
            JOptionPane.showMessageDialog(this, "Usuario Salvao com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            carregarUsuarios();
            limparCampos();
            habilitarDesabilitar(false);
            
        } else {
            
            JOptionPane.showMessageDialog(this, "Erro ao salvar usuario!\nVerifique sua senha!!", "ERRO", JOptionPane.ERROR_MESSAGE);
            
        }
        
    }
    
    private void alterarUsuario(){
        
        modelUsuario.setUsuNome(this.jtfNome.getText());
        modelUsuario.setUsuLogin(this.jtfLogin.getText());
        modelUsuario.setUsuSenha(this.jtfSenha.getText());
        
        if(controllerUsuario.atualizarUsuarioController(modelUsuario) && modelUsuario.getUsuSenha().equalsIgnoreCase(this.jtfConfirmarSenha.getText())){
            JOptionPane.showMessageDialog(this, "Usuario alterado com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
            habilitarDesabilitar(false);
            carregarUsuarios();
            
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar usuario!\nVerifique sua senha!!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    /**
     * Carregar Usuarios na tabela
     */
    private void carregarUsuarios(){
        
        listaModelUsuario = controllerUsuario.listarUsuariosController();
        DefaultTableModel modelo = (DefaultTableModel) jTableUsuario.getModel();
        modelo.setNumRows(0);
        int cont = listaModelUsuario.size();
        
        for (int i = 0; i < cont; i++) {
            
            modelo.addRow(new Object[]{
            
                listaModelUsuario.get(i).getIdUsuario(),
                listaModelUsuario.get(i).getUsuLogin(),
                listaModelUsuario.get(i).getUsuNome(),
                
                
            });
            
        }
        
    }
    
